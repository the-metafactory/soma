type CoworkHomeFileMarkerMatch = "includes" | "startsWith";

interface CoworkStaticHomeFileDescriptor {
  path: string;
  render(input: ProjectionInput): string;
  marker: {
    text: string;
    match: CoworkHomeFileMarkerMatch;
  };
}

