export const ANTHROPIC_COWORK_HOME_FILE_PATHS: readonly string[] = [
  ...ANTHROPIC_COWORK_STATIC_HOME_FILE_DESCRIPTORS.map((file) => file.path),
];

export const ANTHROPIC_COWORK_GENERATED_HOME_FILE_MARKERS = ANTHROPIC_COWORK_STATIC_HOME_FILE_DESCRIPTORS.map((file) => ({
  path: file.path,
  marker: file.marker.text,
  markerMatch: file.marker.match,
}));

export function projectAnthropicCoworkHome(input: ProjectionInput): Projection {
  const instructions = renderInstructions(input);
  const activeVsaFiles = activeVsaBundleFile("anthropic-cowork", input.activeVsa).map((file) => ({
    ...file,
    content: [ANTHROPIC_COWORK_ACTIVE_VSA_MARKER, file.content].join("\n"),
  }));
  return {
    substrate: "anthropic-cowork",
    instructions,
    files: [
      ...ANTHROPIC_COWORK_STATIC_HOME_FILE_DESCRIPTORS.map((file) => ({
        path: file.path,
        content: file.path === ANTHROPIC_COWORK_INSTRUCTIONS_PATH ? instructions : file.render(input),
      })),
      ...activeVsaFiles,
    ],
  };
}

export const anthropicCoworkAdapter: SomaAdapter = {
  name: "anthropic-cowork",
  async detect() {
    try {
      const found = await stat(resolve(homedir(), ".anthropic-cowork"));
      return found.isDirectory();
    } catch {
      return false;
    }
  },
  project(input) {
    return Promise.resolve(projectAnthropicCoworkHome(input));
  },
  run(task: SomaTask) {
    return Promise.resolve({
      taskId: task.id,
      substrate: "anthropic-cowork",
      status: "failed",
      summary: "Anthropic Cowork execution is not implemented; use install/export to generate a standalone projection folder.",
    });
  },
};
