    "",
    "This directory is generated by Soma as a standalone Claude Cowork projection folder.",
    "",
    "This scaffold packages generated snapshots for manual review and Cowork-facing experiments instead of granting the private Soma home; it does not enforce what folders a principal grants to Cowork. Review `memory-snapshot.md` as projected memory input, not as an independent privacy filter.",
    "",
    "## Files",
    "",
    "- `instructions.md` - assistant identity, purpose, and operating rules",
    "- `profile.md` - assistant and principal profile subset",
    "- `purpose.md` - mission, goals, principles, and commitments",
    "- `skills.md` - discovered Soma skills",
    "- `policy.md` - advisory-only policy projection",
    "- `memory-snapshot.md` - projected memory readback snapshot",
    "- `active-vsa.md` - active VSA when one is set",
    "- `<projection-folder>/capture/` - candidate memory and session notes for later review outside Cowork",
    "",
    "Principal-maintained changes do not belong in generated files because they are overwritten. Put Cowork-authored outputs that should be considered for Soma memory under `<projection-folder>/capture/`.",
